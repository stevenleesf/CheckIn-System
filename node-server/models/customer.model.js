const sql = require("./db.js");

// constructor
const Customer = function(customer) {
  this.id = customer.id;
  this.locationname = customer.locationname;
  
};



Customer.create = (newCustomer, result) => {
  sql.query("INSERT INTO location SET ?", newCustomer, (err, res) => {
    if (err) {
      console.log("error: ", err);
      result(err, null);
      return;
    }

    console.log("created customer: ", { id: res.insertId, ...newCustomer });
    result(null, { id: res.insertId, ...newCustomer });
  });
};


Customer.findById = (id, result) => {
  sql.query(`SELECT * FROM customer inner join location on location.id = customer.locationId WHERE id = ${id}`, (err, res) => {
    if (err) {
      console.log("error: ", err);
      result(err, null);
      return;
    }

    if (res.length) {
      console.log("found customer: ", res[0]);
      result(null, res[0]);
      return;
    }

    // not found Customer with the id
    result({ kind: "not_found" }, null);
  });
};

